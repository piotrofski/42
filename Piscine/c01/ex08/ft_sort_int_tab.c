void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	temp;
	int	i_2;

	i_2 = 0;
	while (i_2 < size)
	{
		i = 0;
		while (i < size)
		{
			if ((*(tab + i) > *(tab + (i + 1))) && (i + 1) < size)
			{
				temp = *(tab + (i + 1));
				*(tab + (i + 1)) = *(tab + i);
				*(tab + i) = temp;
			}
			i++;
		}
		i_2++;
	}
}
